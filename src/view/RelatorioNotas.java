    private void preencheTabela() {
    NotaDAO nDAO = new NotaDAO();
    List<Nota> listaNotas = nDAO.listarTodas();

    DefaultTableModel model = (DefaultTableModel) tbl_notas.getModel();
    model.setRowCount(0);

    for (Nota n : listaNotas) {
        model.addRow(new Object[]{
            n.getCodNota(),
            n.getCliente(),
            n.getProduto(),
            n.getData(),
            n.getQuantidade(),
        });
    }
}
    
